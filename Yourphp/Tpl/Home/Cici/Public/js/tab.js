jQuery.fn.TabBind = function (o) {
    o = $.extend({
        Property: "",
        Item: "a"
    }, o || {});
    $(this).find(o.Item).each(function () {
        var idName = $(this).attr("id");
        $("#" + idName).bind("mousemove", function () {
            $("[" + o.Property + "]").each(function () {
                if ($(this).attr(o.Property) == idName) {
                    $(this).show();
                    var tab = "#" + $(this).attr(o.Property);
                    $(tab).addClass("on");
                }
                else {
                    $(this).hide();
                    var tab = "#" + $(this).attr(o.Property);
                    $(tab).removeClass("on");

                }
            });
        });
    });
};

